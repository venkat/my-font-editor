/**
 * Expand compact font data to full stroke objects
 * @param {Object} fontData - Compact font data (optional, uses DEFAULT_FONT_DATA)
 * @param {number} defaultWidth - Default stroke width
 * @returns {Object} Expanded font with stroke objects
 */
export function expandFont(fontData = DEFAULT_FONT_DATA, defaultWidth = 11) {
  const result = {};

  for (const [letter, segments] of Object.entries(fontData)) {
    // Flatten all segments into a single array of strokes
    result[letter] = segments.flat().map(([c1, r1, c2, r2]) => ({
      x1: MX + c1 * SP,
      y1: MY + r1 * SP,
      x2: MX + c2 * SP,
      y2: MY + r2 * SP,
      color: STROKE_COLOR,
      w: defaultWidth
    }));
  }

  return result;
}

/**
 * Get default strokes for a specific letter
 * @param {string} letter - The character to get
 * @param {number} defaultWidth - Default stroke width
 * @returns {Array} Array of stroke objects
 */
export function getDefaultStrokes(letter, defaultWidth = 11) {
  const expanded = expandFont(DEFAULT_FONT_DATA, defaultWidth);
  return expanded[letter] || [];
}
